char	*ft_strcpy(char *s1, const char *s2)
{
	char	*p_s1;

	p_s1 = s1;
	while (*s2)
	{
		*p_s1 = *s2;
		++p_s1;
		++s2;
	}
	*p_s1 = '\0';
	return (s1);
}
