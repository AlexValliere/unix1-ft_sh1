#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*p_s1;

	p_s1 = s1;
	while (*s2)
	{
		if (!n)
			return (s1);
		--n;
		*p_s1 = *s2;
		++p_s1;
		++s2;
	}
	while (n)
	{
		--n;
		*p_s1++ = 0;
	}
	return (s1);
}
