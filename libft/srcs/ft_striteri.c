#include <string.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char	*p_s;
	size_t	i;

	if (s && f)
	{
		p_s = s;
		i = 0;
		while (*p_s)
		{
			(*f)(i, p_s);
			++p_s;
			++i;
		}
	}
}
